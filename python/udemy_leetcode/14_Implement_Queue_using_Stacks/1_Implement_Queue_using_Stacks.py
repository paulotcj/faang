from typing import List, Dict

#-------------------------------------------------------------------------
class MyQueue:
    #-------------------------------------------------------------------------
    def __transfer(self):
        while self.stack1:
            temp = self.stack1.pop()
            self.stack2.append(temp)
    #-------------------------------------------------------------------------
    #-------------------------------------------------------------------------
    def __init__(self):
        self.stack1 = []
        self.stack2 = []
    #-------------------------------------------------------------------------
    #-------------------------------------------------------------------------
    def push(self, x: int) -> None:
        self.stack1.append(x)
    #-------------------------------------------------------------------------
    #-------------------------------------------------------------------------
    def pop(self) -> int:
        if self.stack2:
            return self.stack2.pop()
        
        if self.stack1:
            self.__transfer()
            return self.stack2.pop()
    #-------------------------------------------------------------------------
    #-------------------------------------------------------------------------
    def peek(self) -> int:
        if self.stack2:
            return self.stack2[-1]
        
        if self.stack1:
            self.__transfer()
            return self.stack2[-1]
    #-------------------------------------------------------------------------
    #-------------------------------------------------------------------------
    def empty(self) -> bool:
        return len(self.stack1) == 0 and len(self.stack2) == 0
    #-------------------------------------------------------------------------
